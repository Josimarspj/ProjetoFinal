    private void exibirDadosTabela() {
        try {
            this.carregarListaCargos();
            ModeloDadosFuncionarios modeloDadosFuncionarios = new ModeloDadosFuncionarios();
            this.tblCargosDepartamento.setModel(modeloDadosFuncionarios);
        } catch (ListaCargosVaziaException ex) {
            String titulo = "Lista de Cargos";
            String mensagem = "Lista de Cargos Vazia!";
            JOptionPane.showMessageDialog(this, mensagem, titulo, JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(TelaRelatorioCargoDepartamento.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            String titulo = "Erro ao Carregar Lista de Cargos!";
            String mensagem = "Ocorreu um erro inesperado, Favor entra em contato com o administrador";
            JOptionPane.showMessageDialog(this, mensagem, titulo, JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(TelaRelatorioCargoDepartamento.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public void carregarListaCargos() throws Exception {
        CargoBO cargoBO = new CargoBO();
        cargos = cargoBO.buscarTodos();
    }

