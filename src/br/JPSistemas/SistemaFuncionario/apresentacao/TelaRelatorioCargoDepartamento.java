class ModeloDadosFuncionarios extends AbstractTableModel {

        @Override
        public String getColumnName(int coluna) {
            if (coluna == 0) {
                return "Cargo";
            } else {
                return "Departamento";

            }
        }

        @Override
        public int getRowCount() {
            return cargos.size();
        }

        @Override
        public int getColumnCount() {
            return 2;
        }

        @Override
        public Object getValueAt(int rowIndex, int columnIndex) {
            Cargo cargo = cargos.get(rowIndex);
            if (columnIndex == 0) {
                return cargo.getNome();
            } else {
                return cargo.getDepartamento().getArea();
            }
        }

    }

