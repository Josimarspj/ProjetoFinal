    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        try {
            camposObrigatorios();
            Departamento departamento = new Departamento();
            departamento.setArea(txtArea.getText());
            DepartamentoBO departamentoBO = new DepartamentoBO();
            departamentoBO.inserir(departamento);
            String mensagem = "Departamento Cadastrado com Sucesso!!";
            String titulo = "Cadastro de Departamento";
            JOptionPane.showMessageDialog(this, mensagem, titulo, JOptionPane.INFORMATION_MESSAGE);
            // TODO add your handling code here:
        } catch (CamposObrigatoriosException ex) {
            String titulo = "Erro ao Cadastrar Departamento";
            String mensagem = "Campo Obrigatorio NÃ£o Preenchido!";
            JOptionPane.showMessageDialog(this, mensagem, titulo, JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(TelaCadastroDepartamento.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            String titulo = "Erro ao Cadastrar Departamento";
            String mensagem = "Ocorreu um erro inesperado, Favor entra em contato com o administrador";
            JOptionPane.showMessageDialog(this, mensagem, titulo, JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(TelaCadastroDepartamento.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

